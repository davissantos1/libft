#include "libft.h"

void	ft_putmtx_fd(char **mtx, int fd)
{
	int	i;

	i = -1;
	while (mtx[++i])
	{
		ft_putstr_fd(mtx[i], fd);
		ft_putchar_fd('\n', fd);
	}
}
